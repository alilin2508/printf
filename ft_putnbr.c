#include "../libftprintf.h"

int	ft_putnbr(long n, int *i)
{
	if (n < 0)
	{
		n = n * -1;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10, i);
		ft_putnbr(n % 10, i);
	}
	else
	{
		ft_putchar(n + '0');
		(*i)++;
	}
	return (*i);
}
